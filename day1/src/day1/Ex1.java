package day1;

public class Ex1 {
	public static void main(String[] args) {
		System.out.println("Hello World!");

		System.out.println(3+5+"방가"+4+2);
//		string 뒤에 오는 값들도 string으로 인식됨

		System.out.printf("%s%d","방가",15);
	}
}
/*
main 매서드
public 접근지정자
static 객체를 생성하지 않고도 실행 가능
void return이 없음을 의미
main 자바는 반드시 1개의 main 매서드가 있어야함 시작점
String[] 배열을 의미 args 	인수를 의미
println -> 인쇄 후 줄바꿈 print -> 인쇄 후 줄 안바꿈 */

